package com.fdu.se.sootanalyze.model.xml;

import java.util.Objects;

public class ViewInfo {
	private String id;
	private String name;
	private String callback;
	private String textId;
	private String type;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getCallback() {
		return callback;
	}

	public void setCallback(String callback) {
		this.callback = callback;
	}

	public String getTextId() {
		return textId;
	}

	public void setTextId(String textId) {
		this.textId = textId;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	@Override
	public int hashCode() {
		return Objects.hash(callback, id, name, textId, type);
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if ((obj == null) || (getClass() != obj.getClass()))
			return false;
		ViewInfo other = (ViewInfo) obj;
		return Objects.equals(callback, other.callback) && Objects.equals(id, other.id) && Objects.equals(name, other.name) && Objects.equals(textId, other.textId) && Objects.equals(type, other.type);
	}

	@Override
	public String toString() {
		return String.format("ViewInfo [id=%s, name=%s, callback=%s, textId=%s, type=%s]", id, name, callback, textId, type);
	}

}
